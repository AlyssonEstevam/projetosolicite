    private void btPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btPesquisarMouseClicked
        if(txtPesquisa.getText().equals("") || txtPesquisa.getText().equals("Pesquisar...") || cbFiltro.getSelectedIndex()==0){
            consultar();
        }else{
            PagamentoDao pd = new PagamentoDao();
            ArrayList<Pagamento> pagamentos = new ArrayList();

            pagamentos = pd.consultarFiltro(cbFiltro.getSelectedIndex(), txtPesquisa.getText());

            DefaultTableModel model = (DefaultTableModel) tabelaPagamentos.getModel(); 
            model.setNumRows(0);
            
            DecimalFormat df = new DecimalFormat("#,###0.00");
        
            for(Pagamento p: pagamentos){
                String valorTotal = "R$ "+df.format(p.getValorTotal());
                String valoresAPagar = "R$ "+df.format(p.getValoresAPagar());
                String valoresPagos= "R$ "+df.format(p.getValoresPagos());
                String data = formataData(p.getData());
                String vencimento = formataData(p.getVencimento());
                model.addRow(new Object[]{p.getId(), data, p.getMotivo(), p.getFornecedor(), valorTotal, p.getResponsavel(), valoresAPagar, vencimento, valoresPagos, p.getClassificacao()});
            }
        }
    }//GEN-LAST:event_btPesquisarMouseClicked

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        int linha = tabelaPagamentos.getSelectedRow();
        
        if(linha!=-1){
            int op = JOptionPane.showConfirmDialog(null, "Deseja remover o pagamento "+tabelaPagamentos.getValueAt(linha, 0).toString()+"?", null, JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(op==0){
                PagamentoDao pd = new PagamentoDao();
                Pagamento p = new Pagamento();
                p.setId(Integer.parseInt(tabelaPagamentos.getValueAt(linha, 0).toString()));
                pd.excluir(p);
                consultar();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione o pagamento que deseja remover.");
        }
    }//GEN-LAST:event_btRemoverActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        if(tabelaPagamentos.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "Selecione o pagamento que deseja alterar.");
        }else{
            Pagamento p = new Pagamento();
            p.setId(Integer.parseInt(tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 0).toString()));
            p.setData(tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 1).toString());
            p.setMotivo(tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 2).toString());
            p.setFornecedor(tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 3).toString());
            
            String valorTotal = tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 4).toString();
            valorTotal = valorTotal.substring(3, valorTotal.length()).replace(",", ".");
            p.setValorTotal(Double.parseDouble(valorTotal));
            
            p.setResponsavel(tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 5).toString());
            
            String valoresAPagar = tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 6).toString();
            valoresAPagar = valoresAPagar.substring(3, valoresAPagar.length()).replace(",", ".");
            p.setValoresAPagar(Double.parseDouble(valoresAPagar));
            
            p.setVencimento(tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 7).toString());
            
            String valoresPagos = tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 8).toString();
            valoresPagos = valoresPagos.substring(3, valoresPagos.length()).replace(",", ".");
            p.setValoresPagos(Double.parseDouble(valoresPagos));
            
            p.setClassificacao(tabelaPagamentos.getValueAt(tabelaPagamentos.getSelectedRow(), 9).toString());
            
            TelaAlterarPagamento tap = new TelaAlterarPagamento(p, responsavel);
            tap.setLocationRelativeTo(null);
            tap.setVisible(true);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

