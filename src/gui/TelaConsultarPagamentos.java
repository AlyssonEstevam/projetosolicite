    void consultar(){
        PagamentoDao pd = new PagamentoDao();
        ArrayList<Pagamento> pagamentos = new ArrayList();
        
        pagamentos = pd.consultar();
        
        DefaultTableModel model = (DefaultTableModel) tabelaPagamentos.getModel(); 
        model.setNumRows(0);
        
        DecimalFormat df = new DecimalFormat("#,###0.00");
        
        for(Pagamento p: pagamentos){
            String valorTotal = "R$ "+df.format(p.getValorTotal());
            String valoresAPagar = "R$ "+df.format(p.getValoresAPagar());
            String valoresPagos= "R$ "+df.format(p.getValoresPagos());
            String data = formataData(p.getData());
            String vencimento = formataData(p.getVencimento());
            model.addRow(new Object[]{p.getId(), data, p.getMotivo(), p.getFornecedor(), valorTotal, p.getResponsavel(), valoresAPagar, vencimento, valoresPagos, p.getClassificacao()});
        }
    }
    
    void setarTabela(){
        tabelaPagamentos.getParent().setBackground(Color.white);
        
        tabelaPagamentos.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment( JLabel.CENTER );
        
        for(int i=0; i<10; i++){
            if(i==0){
                tabelaPagamentos.getColumnModel().getColumn(i).setPreferredWidth(50);
            }else if(i==9){
                tabelaPagamentos.getColumnModel().getColumn(i).setPreferredWidth(250);
            }else{
                tabelaPagamentos.getColumnModel().getColumn(i).setPreferredWidth(200);
            }
            tabelaPagamentos.getColumnModel().getColumn(i).setCellRenderer( centerRenderer );
        }
    }
    
    String formataData(String data){
        return data.charAt(8)+""+data.charAt(9)+"/"+data.charAt(5)+""+data.charAt(6)+"/"+data.charAt(0)+""+data.charAt(1)+""+data.charAt(2)+""+data.charAt(3);
    }

