    private void btPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btPesquisarMouseClicked
        if(txtPesquisa.getText().equals("") || txtPesquisa.getText().equals("Pesquisar...") || cbFiltro.getSelectedIndex()==0){
            consultar();
        }else{
            ProdutoDao pd = new ProdutoDao();
            ArrayList<Produto> produtos = new ArrayList();

            produtos = pd.consultarFiltro(cbFiltro.getSelectedIndex(), txtPesquisa.getText());

            DefaultTableModel model = (DefaultTableModel) tabelaProdutos.getModel(); 
            model.setNumRows(0);
            
            DecimalFormat df = new DecimalFormat("#,###0.00");
        
            for(Produto p: produtos){
                String custo = "R$ "+df.format(p.getCustoUnitario());
                String preco = "R$ "+df.format(p.getPreco());
                model.addRow(new Object[]{p.getCodigo(), p.getNome(), p.getDescricao(), p.getMarca(), custo, preco, p.getEstoqueMinimo(), p.getEstoqueAtual(), p.getFornecedor()});
            }
        }
    }//GEN-LAST:event_btPesquisarMouseClicked

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        int linha = tabelaProdutos.getSelectedRow();
        
        if(linha!=-1){
            int op = JOptionPane.showConfirmDialog(null, "Deseja remover o produto "+tabelaProdutos.getValueAt(linha, 1).toString()+"?", null, JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(op==0){
                ProdutoDao pd = new ProdutoDao();
                Produto p = new Produto();
                p.setCodigo(tabelaProdutos.getValueAt(linha, 0).toString());
                pd.excluir(p);
                consultar();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione o produto que deseja remover.");
        }
    }//GEN-LAST:event_btRemoverActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        if(tabelaProdutos.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "Selecione o produto que deseja alterar.");
        }else{
            Produto p = new Produto();
            p.setCodigo(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0).toString());
            p.setNome(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 1).toString());
            p.setDescricao(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 2).toString());
            p.setMarca(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 3).toString());
            String custo = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 4).toString();
            custo = custo.substring(3, custo.length()).replace(",", ".");
            p.setCustoUnitario(Double.parseDouble(custo));
            String preco = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 5).toString();
            preco = preco.substring(3, preco.length()).replace(",", ".");
            p.setPreco(Double.parseDouble(preco));
            p.setEstoqueMinimo(Integer.parseInt(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 6).toString()));
            p.setEstoqueAtual(Integer.parseInt(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 7).toString()));
            p.setFornecedor(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 8).toString());
            
            TelaAlterarProduto tap = new TelaAlterarProduto(p);
            tap.setLocationRelativeTo(null);
            tap.setVisible(true);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

