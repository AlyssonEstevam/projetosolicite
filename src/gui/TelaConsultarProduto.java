    void consultar(){
        ProdutoDao pd = new ProdutoDao();
        ArrayList<Produto> produtos = new ArrayList();
        
        produtos = pd.consultar();
        
        DefaultTableModel model = (DefaultTableModel) tabelaProdutos.getModel(); 
        model.setNumRows(0);
        
        DecimalFormat df = new DecimalFormat("#,###0.00");
        
        
        for(Produto p: produtos){
            String custo = "R$ "+df.format(p.getCustoUnitario());
            String preco = "R$ "+df.format(p.getPreco());
            model.addRow(new Object[]{p.getCodigo(), p.getNome(), p.getDescricao(), p.getMarca(), custo, preco, p.getEstoqueMinimo(), p.getEstoqueAtual(), p.getFornecedor()});
        }
    }
    
    void setarTabela(){
        tabelaProdutos.getParent().setBackground(Color.white);
        
        tabelaProdutos.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment( JLabel.CENTER );
        
        for(int i=0; i<9; i++){
            tabelaProdutos.getColumnModel().getColumn(i).setPreferredWidth(200);
            tabelaProdutos.getColumnModel().getColumn(i).setCellRenderer( centerRenderer );
        }
    }

