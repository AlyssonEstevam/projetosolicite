    void consultar(){
        ClienteDao cd = new ClienteDao();
        ArrayList<Cliente> clientes = new ArrayList();
        
        clientes = cd.consultar();
        
        DefaultTableModel model = (DefaultTableModel) tabelaClientes.getModel(); 
        model.setNumRows(0);
        
        for(Cliente c: clientes){
            String situacao;
            
            if(c.getSituacao()){
                situacao="ATIVO";
            }else{
                situacao="INATIVO";
            }
            
            model.addRow(new Object[]{c.getId(), c.getCpf(), c.getNome(), c.getUsuario1(), c.getUsuario2(), c.getEmail(), c.getTelefone(), situacao});
        }
    }
    
    void setarTabela(){
        tabelaClientes.getParent().setBackground(Color.white);
        
        tabelaClientes.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment( JLabel.CENTER );
        
        for(int i=0; i<8; i++){
            if(i==0){
                tabelaClientes.getColumnModel().getColumn(i).setPreferredWidth(50);
            }else{
                tabelaClientes.getColumnModel().getColumn(i).setPreferredWidth(200);
            }
            tabelaClientes.getColumnModel().getColumn(i).setCellRenderer( centerRenderer );
        }
    }

