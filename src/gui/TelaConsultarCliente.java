    private void btPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btPesquisarMouseClicked
        if(txtPesquisa.getText().equals("") || txtPesquisa.getText().equals("Pesquisar...") || cbFiltro.getSelectedIndex()==0){
            consultar();
        }else{
            ClienteDao cd = new ClienteDao();
            ArrayList<Cliente> clientes = new ArrayList();

            clientes = cd.consultarFiltro(cbFiltro.getSelectedIndex(), txtPesquisa.getText());

            DefaultTableModel model = (DefaultTableModel) tabelaClientes.getModel(); 
            model.setNumRows(0);

            for(Cliente c: clientes){
                String situacao;
            
                if(c.getSituacao()){
                    situacao="ATIVO";
                }else{
                    situacao="INATIVO";
                }

                model.addRow(new Object[]{c.getId(), c.getCpf(), c.getNome(), c.getUsuario1(), c.getUsuario2(), c.getEmail(), c.getTelefone(), situacao});
            }
        }
    }//GEN-LAST:event_btPesquisarMouseClicked

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        int linha = tabelaClientes.getSelectedRow();
        
        if(linha!=-1){
            int op = JOptionPane.showConfirmDialog(null, "Deseja remover o cliente "+tabelaClientes.getValueAt(linha, 2).toString()+"?", null, JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(op==0){
                ClienteDao cd = new ClienteDao();
                Cliente c = new Cliente();
                c.setId(Integer.parseInt(tabelaClientes.getValueAt(linha, 0).toString()));
                cd.excluir(c);
                consultar();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione o cliente que deseja remover.");
        }
    }//GEN-LAST:event_btRemoverActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        if(tabelaClientes.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "Selecione o cliente que deseja alterar.");
        }else{
            Cliente c = new Cliente();
            c.setId(Integer.parseInt(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 0).toString()));
            c.setCpf(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 1).toString());
            c.setNome(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 2).toString());
            c.setUsuario1(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 3).toString());
            c.setUsuario2(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 4).toString());
            c.setEmail(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 5).toString());
            c.setTelefone(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 6).toString());
            if(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 7).toString().equals("ATIVO")){
                c.setSituacao(true);
            }else{
                c.setSituacao(false);
            }
            
            TelaAlterarCliente tac = new TelaAlterarCliente(c);
            tac.setLocationRelativeTo(null);
            tac.setVisible(true);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

