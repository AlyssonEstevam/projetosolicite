    private void btPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btPesquisarMouseClicked
        if(txtPesquisa.getText().equals("") || txtPesquisa.getText().equals("Pesquisar...") || cbFiltro.getSelectedIndex()==0){
            consultar();
        }else{
            UsuarioDao ud = new UsuarioDao();
            ArrayList<Usuario> usuarios = new ArrayList();

            usuarios = ud.consultarFiltro(cbFiltro.getSelectedIndex(), txtPesquisa.getText());

            DefaultTableModel model = (DefaultTableModel) tabelaUsuarios.getModel(); 
            model.setNumRows(0);

            for(Usuario u: usuarios){
                model.addRow(new Object[]{u.getId(), u.getUsuario(), u.getFuncao()});
            }
        }
    }//GEN-LAST:event_btPesquisarMouseClicked

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        int linha = tabelaUsuarios.getSelectedRow();
        
        if(linha!=-1){
            int op = JOptionPane.showConfirmDialog(null, "Deseja remover o usuário "+tabelaUsuarios.getValueAt(linha, 1).toString()+"?", null, JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(op==0){
                UsuarioDao ud = new UsuarioDao();
                Usuario u = new Usuario();
                u.setId(Integer.parseInt(tabelaUsuarios.getValueAt(linha, 0).toString()));
                ud.excluir(u);
                consultar();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione o usuário que deseja remover.");
        }
    }//GEN-LAST:event_btRemoverActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        if(tabelaUsuarios.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "Selecione o usuário que deseja alterar.");
        }else{
            Usuario u = new Usuario();
            u.setId(Integer.parseInt(tabelaUsuarios.getValueAt(tabelaUsuarios.getSelectedRow(), 0).toString()));
            u.setUsuario(tabelaUsuarios.getValueAt(tabelaUsuarios.getSelectedRow(), 1).toString());
            u.setSenha("");
            u.setFuncao(tabelaUsuarios.getValueAt(tabelaUsuarios.getSelectedRow(), 2).toString());
            TelaAlterarUsuario tau = new TelaAlterarUsuario(u);
            tau.setLocationRelativeTo(null);
            tau.setVisible(true);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

