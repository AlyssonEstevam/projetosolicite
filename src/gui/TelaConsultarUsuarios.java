    void consultar(){
        UsuarioDao ud = new UsuarioDao();
        ArrayList<Usuario> usuarios = new ArrayList();
        
        usuarios = ud.consultar();
        
        DefaultTableModel model = (DefaultTableModel) tabelaUsuarios.getModel(); 
        model.setNumRows(0);
        
        for(Usuario u: usuarios){
            model.addRow(new Object[]{u.getId(), u.getUsuario(), u.getFuncao()});
        }
    }
    
    void setarTabela(){
        tabelaUsuarios.getParent().setBackground(Color.white);
        
        tabelaUsuarios.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment( JLabel.CENTER );
        
        for(int i=0; i<3; i++){
            if(i==0){
                tabelaUsuarios.getColumnModel().getColumn(i).setPreferredWidth(50);
            }else{
                tabelaUsuarios.getColumnModel().getColumn(i).setPreferredWidth(200);
            }
            
            tabelaUsuarios.getColumnModel().getColumn(i).setCellRenderer( centerRenderer );
        }
    }

